local opt = vim.opt

-- Line Number
opt.number = true
opt.relativenumber = true

-- Retraction
opt.tabstop = 2
opt.autoindent = true

-- Coursorline
opt.cursorline = true

-- The default new windows are on the right and below
opt.splitright = true
opt.splitbelow = true

-- Theme
opt.termguicolors = true
opt.signcolumn = "yes"

